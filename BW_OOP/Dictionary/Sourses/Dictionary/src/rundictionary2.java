    private void searchFill(String searchTerm){
       DefaultListModel defaultMode = new DefaultListModel(); 
       if( !searchTerm.equals(null)){
        for(int i=0; i<list.size(); i++){
            String startname = list.get(i).word_target.toString().toLowerCase();
            if(startname.contains(searchTerm.toLowerCase())){
                defaultMode.addElement(list.get(i).word_target);
            }
        }
        wordTargetL = defaultMode;
               wordTargetList.setModel(wordTargetL);
        
    }
    else{
    loaddata(list);
}}
    

 void loaddata(ArrayList <Word> list) {
        int i;
        for( i=0; i<list.size(); i++){
            wordTargetList.setModel(wordTargetL);
            wordTargetL.addElement(list.get(i).word_target);
        }
         
    }
