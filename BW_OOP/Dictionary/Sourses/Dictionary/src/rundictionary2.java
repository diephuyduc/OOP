    private void okAddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okAddButtonActionPerformed
            String a = engAddArea.getText();
           if(a.compareTo("")==0||vietAddArea.getText().compareTo("")==0){
             JOptionPane.showMessageDialog(null, "Invalid Adding!");
           }
        else
        {   
            Word newWord = new Word(engAddArea.getText(), vietAddArea.getText());
            if(dictionaryLookup(list, a)==null){
            list.add(newWord);
            
            Collections.sort(list);
            wordTargetList.setModel(wordTargetL);
            wordTargetL.removeAllElements();
            loaddata(list);
            JOptionPane.showMessageDialog(null, "Done!");
            }
            else{
                JOptionPane.showMessageDialog(null, "Invalid Adding!");
            }
        }    
    }//GEN-LAST:event_okAddButtonActionPerformed
 
